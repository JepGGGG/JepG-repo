#include <stdio.h>

int main() {
  int numero, cont , acum ;

  do {
    printf("Ingrese un numero (0 para terminar): ");
    scanf("%d", &numero);

    if (numero != 0) {
  
      cont++;
      acum=acum+numero ;
     
      printf("NÃºmero ingresado: %d\n", numero);
    }
  } while (numero != 0);

  printf("Cantidad de numeros ingresados: %d\n", cont);
  printf("Suma de los numeros ingresados: %d\n", acum);

  return 0;
}
